int main() {
	long double p[] = {12.0, 1.0};
	long double q[] = {13.0, 4.0};
	
	printf("The distance between P and Q is: %Lf\n", l_distance_between_two_points(p, q, 2));

	return 0;
}

//================================================================================
// Function l_distance_between_two_points:    Calculates the distance between two points (p and q) in Rn.
//================================================================================
long double l_distance_between_two_points(long double * p, long double * q, int num_elems) {
	// local declarations
	int i;
	long double res = 0.0;

	//	d(P,Q) = (∑k=1n  sqrt(abs(pk−qk)^2))
	for (i = 0; i < num_elems ; i++)
		res += abs(p[i] - q[i]) * abs(p[i] - q[i]);

	return sqrt(res);
}

